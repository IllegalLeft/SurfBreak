.SECTION "Graphics Routines"

WaitVBlank:
	inc sleeping
-	lda sleeping
	bne -
	rts

LoadPalette:
	lda PPUSTATUS
	lda #$3F
	sta PPUADDR
	lda #$00
	sta PPUADDR

	ldx #$00
-   lda Palettes.w, x
	sta PPUDATA
	inx
	cpx #$20
	bne -
	rts

LoadScreen:
.define src		scratch
	lda #<Map					; setup map src
	sta src
	lda #>Map
	sta src+1
	lda PPUSTATUS
	lda #$20
	sta PPUADDR
	lda #$00
	sta PPUADDR
mvfwd:
	ldy #0
	ldx #3+1
	beq @frag
@page:
	lda (src), y
	sta PPUDATA
	iny
	bne @page
	inc src+1
	dex
	bne @page
@frag:
	cpy #0
	beq @done
	lda (src), y
	sta PPUDATA
	iny
	bne @frag
@done:
	rts

InitPlayerSprite:
	; set tile IDs
	lda #0
	sta OAM.5.tile
	lda #1
	sta OAM.6.tile
	lda #2
	sta OAM.7.tile
	lda #3
	sta OAM.8.tile
	
	; set attributes
	lda #%00000000
	sta OAM.5.attr
	sta OAM.6.attr
	sta OAM.7.attr
	sta OAM.8.attr
	
UpdatePlayerPos:
	lda player.y
	sta scratch
	lda player.y+1
	sta scratch+1
.REPEAT 4
	lsr scratch+1
	ror scratch
.ENDR
	lda scratch

	; set y ordinate
	sta OAM.5.y
	sta OAM.7.y
	clc
	adc #7
	sta OAM.6.y
	sta OAM.8.y
	
	; set x ordinate
	lda player.x
	sta OAM.5.x
	sta OAM.6.x
	clc
	adc #8
	sta OAM.7.x
	sta OAM.8.x
	rts

.ENDS
    

